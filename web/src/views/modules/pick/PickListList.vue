<script>

  import { JeecgListMixin } from '@/mixins/JeecgListMixin'
  import PickListModal from './modules/PickListModal'
  import { getAction } from '@/api/manage'
  import PickDetailList from './PickDetailList'
  import PickOutList from './PickOutList'
  import {initDictOptions,filterMultiDictText} from '@/components/dict/JDictSelectUtil'
  import '@/assets/less/TableExpand.less'

  export default {
    name: "PickListList",
    mixins:[JeecgListMixin],
    components: {
      PickDetailList,
      PickListModal,
      PickOutList
    },
    data () {
      return {
        description: '领料单管理页面',
        // 表头
        columns: [
          {
            title:'领料单号',
            align:"center",
            dataIndex: 'code'
          },
          {
            title:'订单编号',
            align:"center",
            sorter: true,
            dataIndex: 'orderCode'
          },
          {
            title:'领料人',
            align:"center",
            dataIndex: 'pickUser_dictText',
          },
          {
            title:'备注',
            align:"center",
            dataIndex: 'remarks'
          },
          {
            title:'状态',
            align:"center",
            dataIndex: 'status_dictText',
          },
          {
            title:'创建人',
            align:"center",
            dataIndex: 'createBy'
          },
          {
            title:'创建日期',
            align:"center",
            sorter: true,
            dataIndex: 'createTime'
          },
          {
            title:'更新人',
            align:"center",
            dataIndex: 'updateBy'
          },
          {
            title:'更新日期',
            align:"center",
            sorter: true,
            dataIndex: 'updateTime'
          },
          {
            title: '操作',
            dataIndex: 'action',
            align:"center",
            fixed:"right",
            width:147,
            scopedSlots: { customRender: 'action' },
          }
        ],
        url: {
          list: "/pick/pickList/list",
          delete: "/pick/pickList/delete",
          deleteBatch: "/pick/pickList/deleteBatch",
          exportXlsUrl: "/pick/pickList/exportXls",
          importExcelUrl: "pick/pickList/importExcel",
        },
        dictOptions:{
         status:[],
        },
        /* 分页参数 */
        ipagination:{
          current: 1,
          pageSize: 5,
          pageSizeOptions: ['5', '10', '50'],
          showTotal: (total, range) => {
            return range[0] + "-" + range[1] + " 共" + total + "条"
          },
          showQuickJumper: true,
          showSizeChanger: true,
          total: 0
        },
        selectedMainId:'',
        selectedCode: '',
        superFieldList:[],
      }
    },
    created() {
      this.getSuperFieldList();
    },
    computed: {
      importExcelUrl: function(){
        return `${window._CONFIG['domianURL']}/${this.url.importExcelUrl}`;
      }
    },
    methods: {
      initDictConfig(){
        initDictOptions('sys_user,realname,id').then((res) => {
          if (res.success) {
            this.$set(this.dictOptions, 'pickUser', res.result)
          }
        })
        initDictOptions('pick_list_status').then((res) => {
          if (res.success) {
            this.$set(this.dictOptions, 'status', res.result)
          }
        })
      },
      clickThenSelect(record) {
        return {
          on: {
            click: () => {
              this.onSelectChange(record.id.split(","), [record]);
            }
          }
        }
      },
      onClearSelected() {
        this.selectedRowKeys = [];
        this.selectionRows = [];
        this.selectedMainId=''
        this.selectedCode = ''
      },
      onSelectChange(selectedRowKeys, selectionRows) {
        this.selectedMainId=selectedRowKeys[0]
        this.selectedRowKeys = selectedRowKeys;
        this.selectionRows = selectionRows;
        this.selectedCode = selectionRows[0].code;
      },
      loadData(arg) {
        if(!this.url.list){
          this.$message.error("请设置url.list属性!")
          return
        }
        //加载数据 若传入参数1则加载第一页的内容
        if (arg === 1) {
          this.ipagination.current = 1;
        }
        this.onClearSelected()
        var params = this.getQueryParams();//查询条件
        this.loading = true;
        getAction(this.url.list, params).then((res) => {
          if (res.success) {
            this.dataSource = res.result.records;
            this.ipagination.total = res.result.total;
          }
          if(res.code===510){
            this.$message.warning(res.message)
          }
          this.loading = false;
        })
      },
      getSuperFieldList(){
        let fieldList=[];
        fieldList.push({type:'string',value:'code',text:'领料单号',dictCode:''})
        fieldList.push({type:'popup',value:'orderCode',text:'订单编号', popup:{code:'order_popup',field:'code',orgFields:'code',destFields:'order_code'}})
        fieldList.push({type:'string',value:'materialCode',text:'物料编码',dictCode:''})
        fieldList.push({type:'string',value:'materialName',text:'物料名称',dictCode:''})
        fieldList.push({type:'sel_search',value:'pickUser',text:'领料人',dictTable:'sys_user', dictText:'realname', dictCode:'id'})
        fieldList.push({type:'Text',value:'remarks',text:'备注',dictCode:''})
        fieldList.push({type:'string',value:'status',text:'状态',dictCode:'pick_list_status'})
        fieldList.push({type:'string',value:'createBy',text:'创建人',dictCode:''})
        fieldList.push({type:'datetime',value:'createTime',text:'创建日期'})
        fieldList.push({type:'string',value:'updateBy',text:'更新人',dictCode:''})
        fieldList.push({type:'datetime',value:'updateTime',text:'更新日期'})
        this.superFieldList = fieldList
      }
    }
  }
</script>
